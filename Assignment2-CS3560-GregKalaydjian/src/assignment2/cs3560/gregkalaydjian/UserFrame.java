    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        followUser = jTextField1.getText();
        //create a profile for the user and input the user we want to follow
        userDriver.createProfile(lastUser, followUser);
        
        //We want to fill Jlist1 with users we are following
        list1.clear();
        list1.add("Followed Users: ");
        
        followerArrayList = userDriver.checkFollowing(lastUser);
        for (int i = 0; i < followerArrayList.size(); i++)
        {
            list1.add(followerArrayList.get(i));
        }
        
        //clear temp array list
        userDriver.clearTempArray();
        
    }//GEN-LAST:event_jButton5ActionPerformed

